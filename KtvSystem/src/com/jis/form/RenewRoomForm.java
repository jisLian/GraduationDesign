package com.jis.form;

public class RenewRoomForm {
	//续费房间
	private String continueRoom;
	private String conVipTel;
	private String continueTime;
	private String conMoney;
	private String token;
	
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getContinueRoom() {
		return continueRoom;
	}
	public void setContinueRoom(String continueRoom) {
		this.continueRoom = continueRoom;
	}
	
	public String getConVipTel() {
		return conVipTel;
	}
	public void setConVipTel(String conVipTel) {
		this.conVipTel = conVipTel;
	}
	public String getContinueTime() {
		return continueTime;
	}
	public void setContinueTime(String continueTime) {
		this.continueTime = continueTime;
	}
	public String getConMoney() {
		return conMoney;
	}
	public void setConMoney(String conMoney) {
		this.conMoney = conMoney;
	}
	
}
